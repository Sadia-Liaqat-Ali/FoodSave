            <div class="section card-box">
                <h2>Abstract / Introduction</h2>
                <p>
                    The Online Food Waste Management System is a web-based platform designed to connect restaurants, grocery stores, and individuals with NGOs and food banks to efficiently donate surplus food. 
                    This system ensures that excess food is redistributed to those in need instead of being wasted. 
                    The platform enables food donors to list available food items, while NGOs and food banks can browse, request, and schedule pickups for donations. 
                    The system also includes real-time inventory tracking, automatic expiration alerts, and an interactive map to locate nearby food sources. 
                    The goal is to reduce food wastage, promote sustainability, and help combat hunger through a seamless, technology-driven process.
                </p>
            </div>

            <div class="section card-box">
                <h2>Functional Requirements</h2>
                <p>
                    <strong>Admin Panel:</strong> Manage donors/NGOs, view food donations, generate reports, approve/reject registrations, update food safety guidelines.<br>
                    <strong>Food Donor Functions:</strong> Register/login, list donations, update/delete entries, view NGO requests, accept/reject pickups, track past donations and impact.<br>
                    <strong>NGO & Food Bank Functions:</strong> Register/login, browse donations, send pickup requests, track history, provide feedback, locate nearby donors.<br>
                    <strong>Food Collection & Delivery:</strong> Assign drivers, live tracking, automatic alerts for urgent donations, generate receipts.<br>
                    <strong>User Dashboards & Reports:</strong> Donors see past donations & impact; NGOs track requests; Admin generates efficiency reports.
                </p>
